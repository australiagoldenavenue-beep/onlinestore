import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";

import Providers from "@/components/Providers";
import { initializeCron } from "@/lib/cron";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Online Store",
  description: "Generated by create next app",
};

// Initialize cron jobs when the app starts
if (typeof window === 'undefined') {
  initializeCron();
}

import Footer from "@/components/Footer";
import Navbar from "@/components/Navbar";
import { CartProvider } from "@/context/CartContext";
import { prisma } from "@/lib/prisma";

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode
}) {
  // Fetch background image URL from settings
  const bgImageSetting = await prisma.settings.findUnique({
    where: { key: 'backgroundImageUrl' }
  })

  const bgImageUrl = bgImageSetting?.value || ''
  const bgStyle = bgImageUrl ? `url(${bgImageUrl})` : undefined

  return (
    <html lang="en">
      <body
        className={inter.className}
        style={{
          display: 'flex',
          flexDirection: 'column',
          minHeight: '100vh',
          ...(bgStyle && { '--bg-image': bgStyle } as any)
        }}
      >
        <CartProvider>
          <Navbar />
          {children}
          <Footer />
        </CartProvider>
      </body>
    </html>
  )
}
